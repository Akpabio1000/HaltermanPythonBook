import turtle

turtle.penup()
turtle.setposition(-50, -50)
turtle.pencolor('red')
turtle.pendown()
turtle.tracer(0)

for i in range(3):
    turtle.forward(100)
    turtle.left(120)

turtle.update()
turtle.done()
                        
                        
